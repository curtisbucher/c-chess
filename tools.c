int pop_cnt(unsigned long n)
{
    // Counts the number of '1' bits in the given integer.
    return num_ones[n & 0xFF] +
           num_ones[n >> 8 & 0xFF] +
           num_ones[n >> 16 & 0xFF] +
           num_ones[n >> 24 & 0xFF] +
           num_ones[n >> 32 & 0xFF] +
           num_ones[n >> 40 & 0xFF] +
           num_ones[n >> 48 & 0xFF] +
           num_ones[n >> 56 & 0xFF];
}

double timeit(void (*f)(), int n)
{
    // Time the given function and return the result.
    // The function is called n times.
    double t = 0;
    for (int i = 0; i < n; i++)
    {
        clock_t start = clock();
        f();
        clock_t end = clock();
        t += (double)(end - start) * 1000 / CLOCKS_PER_SEC;
    }
    return t / n;
}